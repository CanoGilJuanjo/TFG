            <h2>Normativas de Accesibilidad</h2>
            <p>
                Norma UNE 139803:2004:

                Esta norma establece los Requisitos de Accesibilidad para contenidos en la web en España.
                Es la base de la certificación europea en Accesibilidad Web.
                Real Decreto 1112/2018:

                Este decreto regula la accesibilidad de los sitios web y aplicaciones para dispositivos móviles del
                sector público en España.
                Recoge los requisitos mínimos de accesibilidad que deben cumplir los sitios web y aplicaciones.
                Directiva Europea sobre Accesibilidad Web:

                España transpuso esta directiva a través de normativas como el Real Decreto mencionado.
                Establece estándares para garantizar la accesibilidad de los sitios web y aplicaciones.
                Guía sobre Accesibilidad Web en España:

                Existen recursos como guías que proporcionan consejos sobre cómo crear y establecer requisitos de
                accesibilidad durante el proceso de rediseño web.
                Estas guías ayudan a las empresas a cumplir con las normativas de accesibilidad.
                Cumplir con estas normativas es crucial para promover la inclusión y garantizar que todas las personas
                puedan disfrutar de una experiencia web accesible.
            </p>
