            <h2>Derechos de Autor y Propiedad Intelectual</h2>
            <p>Los derechos de autor y la propiedad intelectual son conceptos legales que protegen las creaciones
                originales de las personas. Aquí tienes una explicación más detallada:

                Derechos de Autor: Se refieren a los derechos legales otorgados a los creadores de obras originales,
                como libros, música, películas, software, obras de arte, entre otros. Estos derechos les otorgan el
                control sobre el uso de sus obras, incluyendo la reproducción, distribución, exhibición pública y
                adaptación de las mismas.

                Propiedad Intelectual: Es un término más amplio que engloba no solo los derechos de autor, sino también
                otros derechos relacionados con la propiedad intelectual, como las patentes y las marcas registradas. La
                propiedad intelectual protege las creaciones de la mente, como inventos, símbolos, nombres, imágenes y
                diseños utilizados en el comercio.

                Ambos conceptos tienen como objetivo proteger y fomentar la creatividad y la innovación, otorgando a los
                creadores el reconocimiento y la recompensa por sus esfuerzos creativos, al mismo tiempo que promueven
                el intercambio de conocimientos y la competencia justa en el mercado.
