            <h2>Ley de Protección de Datos Personales</h2>
            <p>
                La Ley de Protección de Datos Personales es una normativa legal que tiene como objetivo proteger la
                privacidad de las personas en relación con el tratamiento de sus datos personales. Aquí tienes una
                explicación más detallada:

                Objetivo: La ley de protección de datos tiene como objetivo garantizar que el tratamiento de los datos
                personales sea realizado de manera justa y transparente, protegiendo los derechos fundamentales de las
                personas en lo que respecta a la privacidad y la protección de sus datos personales.

                Alcance: Esta ley regula el tratamiento de los datos personales por parte de entidades públicas y
                privadas, estableciendo los principios, derechos y obligaciones que deben cumplir aquellos que tratan
                datos personales.

                Principios: Los principios fundamentales de la ley suelen incluir el consentimiento del titular de los
                datos para su tratamiento, la finalidad legítima del tratamiento, la exactitud de los datos, la
                limitación en el uso de los datos, la seguridad de la información, entre otros.

                Derechos de los Titulares: La ley también reconoce y garantiza los derechos de los titulares de los
                datos, como el acceso, rectificación, cancelación y oposición al tratamiento de sus datos personales.

                Sanciones: La ley establece sanciones en caso de incumplimiento, con el fin de garantizar el
                cumplimiento de las disposiciones de protección de datos.

                En resumen, la Ley de Protección de Datos Personales busca regular el tratamiento de la información
                personal, asegurando que se realice de manera ética y responsable, protegiendo los derechos
                fundamentales de las personas en lo que respecta a su privacidad y el uso de sus datos personales.
            </p>
